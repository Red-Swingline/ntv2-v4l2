struct ntv2_hdmiedid *ntv2_hdmiedid_open(struct ntv2_object *ntv2_obj,
										 const char *name, int index)
{
	struct ntv2_hdmiedid *ntv2_hed = NULL;

	ntv2_hed = kzalloc(sizeof(struct ntv2_hdmiedid), GFP_KERNEL);
	if (ntv2_hed == NULL) {
		NTV2_MSG_ERROR("%s: ntv2_hdmiedid instance memory allocation failed\n", ntv2_obj->name);
		return NULL;
	}

	ntv2_hed->index = index;
	snprintf(ntv2_hed->name, NTV2_STRING_SIZE, "%s-%s%d", ntv2_obj->name, name, index);
	INIT_LIST_HEAD(&ntv2_hed->list);
	ntv2_hed->ntv2_dev = ntv2_obj->ntv2_dev;

	NTV2_MSG_HDMIIN_INFO("%s: open ntv2_hdmiedid\n", ntv2_hed->name);

	return ntv2_hed;
}

void ntv2_hdmiedid_close(struct ntv2_hdmiedid *ntv2_hed)
{
	if (ntv2_hed == NULL) 
		return;

	NTV2_MSG_HDMIIN_INFO("%s: close ntv2_hdmiedid\n", ntv2_hed->name);

	memset(ntv2_hed, 0, sizeof(struct ntv2_hdmiedid));
	kfree(ntv2_hed);
}

int ntv2_hdmiedid_configure(struct ntv2_hdmiedid *ntv2_hed,
							enum ntv2_edid_type type,
							int	port_index)
{
	u8 *edid;
	u32 size;
	u32 start;
	u32 end;
	u32 count;
	u32 prefix_size;
	u32 edid_size;
	u32 sum;
	bool modify_port = false;
	u32 i;
	u32 j;
	
	if (ntv2_hed == NULL) 
		return -EPERM;

	NTV2_MSG_HDMIIN_INFO("%s: configure hdmiedid\n", ntv2_hed->name);

	switch (type)
	{
	case ntv2_edid_type_konahdmi_20:
		edid = c_edid_konahdmi_20;
		size = sizeof(c_edid_konahdmi_20);
		modify_port = true;
		break;
	case ntv2_edid_type_konahdmi_13:
		edid = c_edid_konahdmi_13;
		size = sizeof(c_edid_konahdmi_13);
		modify_port = true;
		break;
	case ntv2_edid_type_corvidhbr:
		edid = c_edid_corvidhbr;
		size = sizeof(c_edid_corvidhbr);
		modify_port = false;
		break;
	case ntv2_edid_type_io4k:
		edid = c_edid_io4k;
		size = sizeof(c_edid_io4k);
		modify_port = false;
		break;
	case ntv2_edid_type_io4kplus:
		edid = c_edid_io4kplus;
		size = sizeof(c_edid_io4kplus);
		modify_port = false;
		break;
	default:
		NTV2_MSG_HDMIIN_ERROR("%s: *error* unknown edid type %d\n", ntv2_hed->name, type);
		return -EPERM;
	}

	if (size > NTV2_HDMI_EDID_SIZE) {
		NTV2_MSG_HDMIIN_ERROR("%s: *error* unexpected edid size %d\n", ntv2_hed->name, size);
		return -EPERM;
	}

	if (port_index >= sizeof(c_hdmi_port_number)) {
		NTV2_MSG_HDMIIN_ERROR("%s: *error* unexpected port index %d\n", ntv2_hed->name, port_index);
		return -EPERM;
	}

	/* copy static edid to buffer */
	memcpy(ntv2_hed->edid_data, edid, size);
	ntv2_hed->edid_size = size;

	if (modify_port) {
		/* locate and modify edid port number */
		prefix_size = sizeof(c_hdmi_port_pefix);
		edid_size = size - prefix_size - 1;
		for (i = 0; i < edid_size; i++) {
			for (j = 0; j < prefix_size; j++) {
				if (c_hdmi_port_pefix[j] != ntv2_hed->edid_data[i + j])
					break;
			}
			if (j == prefix_size) {
				ntv2_hed->edid_data[i + prefix_size] = c_hdmi_port_number[port_index];
				break;
			}
		}
		if (i == edid_size) {
			NTV2_MSG_HDMIIN_ERROR("%s: *error* cannot modify port number\n", ntv2_hed->name);
		}
	}

	/* compute new checksums */
	count = ntv2_hed->edid_size / 128;
	for (i = 0; i < count; i++) {
		sum = 0;
		start = i * 128;
		end = start + 127;
		for (j = start; j < end; j++) {
			sum += ntv2_hed->edid_data[j];
		}
		ntv2_hed->edid_data[end] = (u8)((~sum + 1) & 0xff);
	}
	
	return 0;
}

u8 *ntv2_hdmi_get_edid_data(struct ntv2_hdmiedid *ntv2_hed)
{
	if (ntv2_hed == NULL) 
		return NULL;
	
	return ntv2_hed->edid_data;
}

u32 ntv2_hdmi_get_edid_size(struct ntv2_hdmiedid *ntv2_hed)
{
	if (ntv2_hed == NULL) 
		return 0;
	
	return ntv2_hed->edid_size;
}

